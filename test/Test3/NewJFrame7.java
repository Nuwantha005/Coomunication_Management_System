    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            JFileChooser fileChooser = new JFileChooser(System.getProperty("user.home") + "\\Music\\English Songs");
            fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            int result = fileChooser.showOpenDialog(this);
            // user clicked Cancel button on dialog
            if (result == JFileChooser.CANCEL_OPTION) {
                file = null;
            } else {
                file = fileChooser.getSelectedFile();
            }
            removePreviousPlayer();
            player = Manager.createPlayer(file.toURL());
            player.addControllerListener(new NewJFrame7.EventHandler());
            player.start();
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame7.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoPlayerException ex) {
            Logger.getLogger(NewJFrame7.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    private void removePreviousPlayer() {
        if (player == null) {
            return;
        }
        Component visual = player.getVisualComponent();
        Component control = player.getControlPanelComponent();
        Container c = getContentPane();
        if (visual != null) {
            c.remove(visual);
        }
        if (control != null) {
            c.remove(control);
        }
        player.close();
    }

