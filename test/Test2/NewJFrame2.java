    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        DefaultPieDataset set = new DefaultPieDataset();
        try {
            String sql = "select distinct date from sales ";
            ResultSet rs = MyConn.search(sql);
            while (rs.next()) {
                String sql2 = "select sum(total) from sales where date = '" + rs.getString(1) + "' ";
                ResultSet rs2 = MyConn.search(sql2);
                while (rs2.next()) {
                    dataset.setValue(rs2.getDouble(1), "total", rs.getString(1));
                    set.setValue(rs.getString(1), rs2.getDouble(1));
                }
            }
            if (panel != null) {
                panel.setVisible(false);
            }
            if (panel2 != null) {
                panel2.setVisible(false);
            }
            if (jComboBox1.getSelectedIndex() == 0) {
                panel = new ChartPanel(ChartFactory.createBarChart3D("Sales", "Date", "Income", dataset, PlotOrientation.VERTICAL, true, true, true));
            } else if (jComboBox1.getSelectedIndex() == 1) {
                panel = new ChartPanel(ChartFactory.createLineChart3D("Sales", "Date", "Income", dataset, PlotOrientation.VERTICAL, true, true, true));
            } else if (jComboBox1.getSelectedIndex() == 2) {
                panel = new ChartPanel(ChartFactory.createWaterfallChart("Sales Income", "Date", "Income", dataset, PlotOrientation.VERTICAL, true, true, true));
            } else if (jComboBox1.getSelectedIndex() == 4) {
                panel = new ChartPanel(ChartFactory.createPieChart3D("Sales Income", set, true, true, true));
            } else if (jComboBox1.getSelectedIndex() == 3) {
                panel = new ChartPanel(ChartFactory.createRingChart("Sales Income", set, true, true, true));
            }
            getContentPane().add(panel);
            panel.setBounds(30, 30, 900, 500);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

