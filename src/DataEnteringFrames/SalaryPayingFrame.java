    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        try {

            Date d = new Date();
            if (!Today.isSelected() && Now.isSelected()) {
                if (Today.isSelected()) {
                    d.setHours(Integer.parseInt(hours.getText()));
                    d.setMinutes(Integer.parseInt(minitues.getText()));
                    d.setSeconds(Integer.parseInt(seconds.getText()));
                } else {
                    d = date.getSelectedDate().getTime();
                    d.setHours(Integer.parseInt(hours.getText()));
                    d.setMinutes(Integer.parseInt(minitues.getText()));
                    d.setSeconds(Integer.parseInt(seconds.getText()));
                }
            }
            java.sql.Date salaryPayDate = new java.sql.Date(Year.getYear() - 1900, Month.getMonth(), 1);
            java.sql.Date date = new java.sql.Date(d.getTime());
            Time time = new Time(d.getTime());
            String sql = "update  salarypaying set date = '" + date + "',time = '" + time + "',salaryfor = '" + salaryPayDate + "',staff_sid = '" + Staff.getSelectedItem().toString().split(" - ")[0] + "' where id = '" + id.getText() + "' ";
            MyConn.save(sql);
            JOptionPane.showMessageDialog(this, "Completed", "Message", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdateActionPerformed

