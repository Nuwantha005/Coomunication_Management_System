    private void NowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NowActionPerformed

        if (Now.isSelected()) {
            t.start();
            Today.setSelected(true);
            date.setEnabled(false);
            hours.setEnabled(false);
            minitues.setEnabled(false);
            seconds.setEnabled(false);
        } else {
            t.stop();
            date.setEnabled(true);
            hours.setEnabled(true);
            minitues.setEnabled(true);
            seconds.setEnabled(true);
            hours.setText(null);
            minitues.setText(null);
            seconds.setText(null);
        }
    }//GEN-LAST:event_NowActionPerformed

