    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        try {
            String search = "select * from due where dueid = '" + DueID.getText() + "' ";
            ResultSet rs = MyConn.search(search);
            while (rs.next()) {
                if (rs.getBoolean(2)) {
                    DueType.setSelectedIndex(0);
                } else {
                    DueType.setSelectedIndex(1);
                }
                FinishingDate.setText(rs.getString(3));
                if (rs.getString(4) == null) {
                    FinishedDate.setText("Not Finished Yet");
                    FinishedTime.setText("Not Finished Yet");
                } else {
                    FinishedDate.setText(rs.getString(4));
                    FinishedTime.setText(rs.getString(5));
                    Calendar selectedDate = dateChooserCombo1.getSelectedDate();
                    selectedDate.setTime(rs.getDate(4));
                    dateChooserCombo1.setSelectedDate(selectedDate);
                    String[] time = rs.getString(5).split(":");
                    Hours.setText(time[0]);
                    Minitues.setText(time[1]);
                    Seconds.setText(time[2]);
                }
                if (rs.getBoolean(2)) {
                    String getGRNlData = "select * from grn where due_dueid = '" + DueID.getText() + "' ";
                    ResultSet GRNData = MyConn.search(getGRNlData);
                    while (GRNData.next()) {
                        String getSupplier = "select sname from suppliers where sid = '" + GRNData.getString(3) + "' ";
                        ResultSet supplier = MyConn.search(getSupplier);
                        while (supplier.next()) {
                            CustomerSupplier.setText(GRNData.getString(3) + " - " + supplier.getString(1));
                        }
                        Invoice.setText(GRNData.getString(1));
                        Date.setText(GRNData.getString(4));
                        Time.setText(GRNData.getString(5));
                        String getItemMinSellingPrice = "select iminprice from stock where iid = '" + GRNData.getString(2) + "' ";
                        ResultSet itemMinSellingPrice = MyConn.search(getItemMinSellingPrice);
                        while (itemMinSellingPrice.next()) {
                            Value.setText(itemMinSellingPrice.getDouble(1) * GRNData.getInt(6) + "");
                        }
                    }
                } else {
                    String getSaleData = "select * from sales where due_dueid  = '" + DueID.getText() + "' ";
                    ResultSet saleData = MyConn.search(getSaleData);
                    while (saleData.next()) {
                        String getCustomerData = "select cusname from customers where cusid = '" + saleData.getString(2) + "' ";
                        ResultSet customerData = MyConn.search(getCustomerData);
                        while (customerData.next()) {
                            CustomerSupplier.setText(saleData.getString(2) + " - " + customerData.getString(1));
                        }
                        Invoice.setText(saleData.getString(1));
                        Date.setText(saleData.getString(3));
                        Time.setText(saleData.getString(4));
                        Value.setText(saleData.getString(7));
                    }

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SearchActionPerformed

