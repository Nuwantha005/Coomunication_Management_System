    private void jCheckBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox2ActionPerformed

        if (jCheckBox2.isSelected()) {
            Date date = new Date();
            Calendar cal = dateChooserCombo1.getSelectedDate();
            cal.setTime(date);
            dateChooserCombo1.setSelectedDate(cal);
            jCheckBox3.setSelected(true);
            dateChooserCombo1.setEnabled(false);
            Hours.setEnabled(false);
            Minitues.setEnabled(false);
            Seconds.setEnabled(false);
            t.start();
        } else {
            dateChooserCombo1.setEnabled(true);
            Hours.setEnabled(true);
            Minitues.setEnabled(true);
            Seconds.setEnabled(true);
            t.stop();
        }

    }//GEN-LAST:event_jCheckBox2ActionPerformed

    private void FinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinishActionPerformed
        try {
            String duetto = "";
            if (DueType.getSelectedIndex() == 0) {
                duetto = "suppliers";
            } else {
                duetto = "sales";
            }
            Date date = new Date();
            if (!jCheckBox2.isSelected()) {
                if (!jCheckBox3.isSelected()) {
                    date.setTime(dateChooserCombo1.getSelectedDate().getTimeInMillis());
                }
                date.setHours(Integer.parseInt(Hours.getText()));
                date.setMinutes(Integer.parseInt(Minitues.getText()));
                date.setSeconds(Integer.parseInt(Seconds.getText()));
            }
            java.sql.Date sqlDate = new java.sql.Date(date.getTime());
            Time time = new Time(date.getTime());
            String sql = "select *from due where dueid = '" + DueID.getText() + "' ";
            ResultSet rs = MyConn.search(sql);
            while (rs.next()) {
                String updateDue = "update due set duefinisheddate = '" + sqlDate + "',duefinishedtime = '" + time + "' where dueid = '" + DueID.getText() + "' ";
                MyConn.save(updateDue);
            }
            JOptionPane.showMessageDialog(this, "Completed", "Message", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_FinishActionPerformed

