    private void BuyingPriceKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BuyingPriceKeyReleased
        // TODO add your handling code here:
        try {
            String sql = "select iminprice from stock where iid = '" + Item.getSelectedItem().toString().split(" - ")[0] + "' ";
            ResultSet rs = MyConn.search(sql);
            while (rs.next()) {
                if (rs.getDouble(1) < Double.parseDouble(BuyingPrice.getText()) || rs.getDouble(1) == Double.parseDouble(BuyingPrice.getText())) {
                    JOptionPane.showMessageDialog(this, "The Buying Price is Greate than Items's Min Selling Price", "Buying Price Problem", JOptionPane.WARNING_MESSAGE);
                    BuyingPrice.setText(BuyingPrice.getText().substring(0, BuyingPrice.getText().length() - 1));
                } else {
                    Value.setText(Double.parseDouble(BuyingPrice.getText()) * Integer.parseInt(Count.getText()) + "");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_BuyingPriceKeyReleased

