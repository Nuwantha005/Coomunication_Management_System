    private void itemCodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_itemCodeKeyReleased
        try {
            if (list != null) {
                list.setVisible(false);
            }
            if (!itemCode.getText().equals("")) {
                String sql = "select iid,iname from stock where icode like '" + itemCode.getText() + "%' ";
                ResultSet rs = MyConn.search(sql);
                Vector v = new Vector();
                while (rs.next()) {
                    v.add(rs.getString(1) + " - " + rs.getString(2));
                }
                list = new JList(v);
                getContentPane().add(list);
                list.setBounds(itemCode.getX(), itemCode.getY() + itemCode.getHeight(), itemCode.getWidth(), v.size() * 27);
                list.setFont(new Font("Tahoma", Font.PLAIN, 18));
                jPanel1.setComponentZOrder(list, 0);
                list.addMouseListener(new java.awt.event.MouseAdapter() {

                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        for (int i = 0; i < Item.getItemCount(); i++) {
                            if (list.getSelectedValue() != null) {
                                if (Item.getItemAt(i).toString().split(" - ")[0].equals(list.getSelectedValue().toString().split(" - ")[0])) {
                                    Item.setSelectedIndex(i);
                                }
                            }
                            list.setVisible(false);
                        }
                    }
                });
                list.addKeyListener(new java.awt.event.KeyAdapter() {

                    public void keyTyped(java.awt.event.KeyEvent evt) {
                        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
                            list.setSelectedIndex(list.getSelectedIndex() + 1);
                        } else if (evt.getKeyCode() == KeyEvent.VK_UP) {
                            list.setSelectedIndex(list.getSelectedIndex() - 1);
                        }
                    }
                });
            } else {
                if (list != null) {
                    list.setVisible(false);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_itemCodeKeyReleased

