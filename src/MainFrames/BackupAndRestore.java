    private void restoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_restoreActionPerformed
        try {
            Runtime runtime = Runtime.getRuntime();
            String[] restoreCmd = new String[]{"mysql ", "--user=" + "root", "--password=123", "-e", "source " + restorePath.getText()};
            Process pro = runtime.exec(restoreCmd);
            int proComplete = pro.waitFor();
            if (proComplete == 0) {
                JOptionPane.showMessageDialog(this, "Restore Completed ");
            } else {
                JOptionPane.showMessageDialog(this, "Restore Not Completed ", "Error", JOptionPane.ERROR_MESSAGE);
            }
            restoreing.setText("");
            Robot robot = new Robot();
            Thread.sleep(1000);
            loadingRestore.requestFocus();
            Point p = loadingRestore.getLocationOnScreen();
            loadingRestore.setIcon(new ImageIcon(robot.createScreenCapture(new Rectangle(p, loadingBackup.getSize()))));
        } catch (IOException | InterruptedException | HeadlessException | AWTException e) {
            JOptionPane.showMessageDialog(this, "Error :  " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_restoreActionPerformed

