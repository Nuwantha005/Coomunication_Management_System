    private void backupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backupActionPerformed
        try {
            loadingBackup.setIcon(new ImageIcon(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/Images/Backuping.gif"))));
            backuping.setText("Backuping....");
            Runtime runtime = Runtime.getRuntime();
            Process pro = runtime.exec("mysqldump -uroot -p123 --add-drop-database -B communication_software -r " + backupPath.getText() +  " ");
            int proComplete = pro.waitFor();
            if (proComplete == 0) {
                JOptionPane.showMessageDialog(this, "Backup Completed ");
            } else {
                JOptionPane.showMessageDialog(this, "Backup Not Completed ", "Error", JOptionPane.ERROR_MESSAGE);
            }
            Robot robot = new Robot();
            Thread.currentThread().sleep(1000);
            loadingBackup.requestFocus();
            Point p = loadingBackup.getLocationOnScreen();
            backuping.setText("");
            loadingBackup.setIcon(new ImageIcon(robot.createScreenCapture(new Rectangle(p, loadingBackup.getSize()))));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error :  " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_backupActionPerformed

    private void BrowseBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseBackupActionPerformed
        JFileChooser restoreChooser = new JFileChooser(System.getProperty("user.hoime") + "/Documents");
        restoreChooser.setFileFilter(new FileNameExtensionFilter("sqlFile", "sql"));
        if (restoreChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            backupFile = restoreChooser.getSelectedFile();
            if (backupFile.getAbsolutePath().contains(".")) {
                backupPath.setText(backupFile.getAbsolutePath());
            } else {
                backupPath.setText(backupFile.getAbsolutePath() + ".sql");
            }

        } else {
            JOptionPane.showMessageDialog(this, "No File Selected", "File Problem", 0);
        }
    }//GEN-LAST:event_BrowseBackupActionPerformed

