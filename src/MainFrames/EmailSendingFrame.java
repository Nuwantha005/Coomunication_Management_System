    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed

        try {
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            int x = jTable1.getRowCount();
            for (int i = 0; i < x; i++) {
                df.removeRow(0);
            }
            if (jComboBox1.getSelectedIndex() == 0) {
                String sql = "select sid,sname,semail from suppliers ";
                ResultSet rs = MyConn.search(sql);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString(1) + " - " + rs.getString(2));
                    v.add(rs.getString(3));
                    df.addRow(v);
                }
            } else if (jComboBox1.getSelectedIndex() == 1) {
                String sql = "select sid,sfirstname,slastname,semail from staff ";
                ResultSet rs = MyConn.search(sql);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString(1) + " - " + rs.getString(2) + " " + rs.getString(3));
                    v.add(rs.getString(4));
                    df.addRow(v);
                }
            } else if (jComboBox1.getSelectedIndex() == 2) {
                String sql = "select cusid,cusname,cusemail from customers ";
                ResultSet rs = MyConn.search(sql);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString(1) + " - " + rs.getString(2));
                    v.add(rs.getString(3));
                    df.addRow(v);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

